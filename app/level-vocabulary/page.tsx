// פונקציה ליצירת אוצר מילים מכל השאלות
const generateVocabularyFromQuestions = (questions: any[], gameType: string): { en: string; he: string; category: string; source: string; gameType: string }[] => {
  const vocabularyMap = new Map<string, { he: string; category: string; source: string; gameType: string }>();
  
  questions.forEach(question => {
    // חילוץ מילים מהשאלה עצמה (החשוב ביותר!)
    const questionWords = extractEnglishWords(question.text || question.sentence || '');
    const explanationWords = extractEnglishWords(question.explanation || '');
    
    // מילים מהשאלה עצמה (עדיפות ראשונה!)
    questionWords.forEach((word) => {
      const wordLower = word.toLowerCase();
      if (!vocabularyMap.has(wordLower)) {
        vocabularyMap.set(wordLower, {
          he: TRANSLATION_DICT[wordLower] || '',
          category: 'general',
          source: 'question',
          gameType: gameType
        });
      }
    });
    
    // מילים מההסבר
    explanationWords.forEach((word) => {
      const wordLower = word.toLowerCase();
      if (!vocabularyMap.has(wordLower)) {
        vocabularyMap.set(wordLower, {
          he: TRANSLATION_DICT[wordLower] || '',
          category: 'general',
          source: 'explanation',
          gameType: gameType
        });
      }
    });
    
    // מילים מהאפשרויות (אם קיימות)
    if (question.options) {
      question.options.forEach((option: string) => {
        const optionWords = extractEnglishWords(option);
        optionWords.forEach(word => {
          if (!vocabularyMap.has(word.toLowerCase())) {
            vocabularyMap.set(word.toLowerCase(), {
              he: TRANSLATION_DICT[word.toLowerCase()] || '',
              category: 'general',
              source: 'options',
              gameType: gameType
            });
          }
        });
      });
    }
    
    // מילה מהתשובה (אם קיימת)
    if (question.answer && typeof question.answer === 'string') {
      const answerWords = extractEnglishWords(question.answer);
      answerWords.forEach(word => {
        if (!vocabularyMap.has(word.toLowerCase())) {
          vocabularyMap.set(word.toLowerCase(), {
            he: TRANSLATION_DICT[word.toLowerCase()] || '',
            category: 'general',
            source: 'answer',
            gameType: gameType
          });
        }
      });
    }
  });
  
  return Array.from(vocabularyMap.entries()).map(([en, data]) => ({
    en,
    ...data
  }));
};

